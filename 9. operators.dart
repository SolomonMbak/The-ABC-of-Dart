main() {
  // Addition
  int num_1 = 5 + 10;
  
  // Subtraction
  int num_2 = 20 - 5;

  // Divide
  double num_3 = 20 / 2;

  // Modulo operator
  int num_4 = num_2 % 3;

  // Multiply num_2 by 2
  int num_5 = num_2 *= 2; //This means num_2 = num_2 * 2


  print(num_1);
  print(num_2);
  print(num_3);
  print(num_4);
  print(num_5);
}